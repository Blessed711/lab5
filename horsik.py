# Зчитуємо два числа: N — кількість клітинок, K — максимальна довжина стрибка
N, K = map(int, input().split())

# Створюємо масив dp, де dp[i] — кількість способів дістатися до клітинки i
dp = [0] * (N + 1)
dp[1] = 1  # Є лише один спосіб дістатися до клітинки 1 — з неї починаємо

# Обчислюємо кількість способів дістатися до кожної клітинки від 2 до N
for i in range(2, N + 1):
    for j in range(1, K + 1):
        if i - j >= 1:
            dp[i] += dp[i - j]  # Додаємо кількість способів дістатися до попередніх клітинок

# Виводимо кількість способів дістатися до останньої клітинки
print(dp[N])
