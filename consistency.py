# Зчитуємо кількість елементів послідовності
n = int(input())

# Зчитуємо саму послідовність
arr = list(map(int, input().split()))

# Ініціалізуємо масив dp: dp[i] — довжина найдовшої послідовнократної підпослідовності, що закінчується в arr[i]
dp = [1] * n  # Кожен елемент сам по собі — це підпослідовність довжини 1

# Обчислюємо значення dp для кожного елемента
for i in range(n):
    for j in range(i):
        if arr[i] % arr[j] == 0:
            dp[i] = max(dp[i], dp[j] + 1)  # Якщо arr[i] кратне arr[j], оновлюємо dp[i]

# Виводимо максимальну довжину серед усіх можливих підпослідовностей
print(max(dp))
