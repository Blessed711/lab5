# Зчитуємо кількість фільмів, які переглянули Василь, Петро та Тарас
n, m, k = map(int, input().split())

# Зчитуємо множини фільмів, які переглянув кожен з них
vasyl = set(map(int, input().split()))  # Фільми Василя
petro = set(map(int, input().split()))  # Фільми Петра
taras = set(map(int, input().split()))  # Фільми Тараса

# Знаходимо перетин трьох множин — тобто фільми, які бачили всі троє
common = vasyl & petro & taras

# Виводимо кількість спільних фільмів
print(len(common))

# Виводимо номери цих фільмів у зростаючому порядку
print(*sorted(common))
