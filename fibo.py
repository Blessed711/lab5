# Зчитуємо число N — індекс числа Фібоначчі, яке потрібно знайти
n = int(input())

# Ініціалізуємо перші два числа послідовності: F₀ = 0, F₁ = 1
a, b = 0, 1

# Обчислюємо N-те число Фібоначчі
for _ in range(n):
    a, b = b, a + b   # Наступне число = сума двох попередніх

# Виводимо результат: Fₙ
print(a)
