# Зчитуємо число N — останній індекс числа Фібоначчі, яке потрібно врахувати
n = int(input())

# Створюємо список для збереження чисел Фібоначчі
fibo = [0, 1]  # Початкові значення: F₀ = 0, F₁ = 1

# Обчислюємо всі наступні числа Фібоначчі до Fₙ
for i in range(2, n + 1):
    fibo.append(fibo[i - 1] + fibo[i - 2])  # Fᵢ = Fᵢ₋₁ + Fᵢ₋₂

# Обчислюємо суму перших (n + 1) чисел Фібоначчі: F₀ + F₁ + ... + Fₙ
print(sum(fibo[:n + 1]))
